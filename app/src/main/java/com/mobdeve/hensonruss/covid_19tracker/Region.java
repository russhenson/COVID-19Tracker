package com.mobdeve.hensonruss.covid_19tracker;

public class Region {
    private String region;
    private int cases;
    private int recovered;
    private int deaths;

    public String getRegion() {
        return region;
    }

    public int getCases() {
        return cases;
    }

    public int getRecovered() {
        return recovered;
    }

    public int getDeaths() {
        return deaths;
    }
}
